    private boolean validarEmprestimo() {
        boolean valido = true;
        if (emprestimoCtrl.getAluno().getID() == null) {
            labelErrMatricula.setText("* selecione um aluno");
            valido = false;
        }
        if (emprestimoCtrl.getLivro().getID() == null) {
            labelErrIdLivro.setText("* selecione um livro");
            valido = false;
        }
        try {
            int q = Integer.parseInt(textFieldQuantidade.getText());
            if (q <= 0) {
                labelErrQuantidade.setText("* quantidade deve ser acima de 0");
                valido = false;
            }
            if (emprestimoCtrl.getLivro().getQntEstante() == 0) {
                labelErrQuantidade.setText("* não há exemplares deste título nas estantes");
                valido = false;
            } else if (emprestimoCtrl.getLivro().getQntEstante() - q < 0) {
                labelErrQuantidade.setText("* quantidade deve ser maior ou igual ao total de exemplares");
                valido = false;
            }
        } catch (Exception ex) {
            labelErrQuantidade.setText("* quantidade inválida");
            valido = false;
        }
        try {
            Date prazo = new SimpleDateFormat("dd/MM/yyyy").parse(textFieldPrazo.getText());
            if (prazo.compareTo(new Date()) <= 0) {
                labelErrPrazo.setText("* prazo inválido");
                valido = false;
            }
        } catch (Exception ex) {
            labelErrPrazo.setText("* data inválida");
            valido = false;
        }
        return valido;
    }

