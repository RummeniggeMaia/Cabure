    private void botaoProcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoProcessarActionPerformed
        if (validarEmprestimo()) {
            Emprestimo e = new Emprestimo();
            try {
                int qnt = Integer.parseInt(textFieldQuantidade.getText());
                e.setQuantidade(qnt);
                e.setPrazo(new SimpleDateFormat("dd/MM/yyyy HH:mm:ss").parse(textFieldPrazo.getText() + " 23:59:59"));
                e.setDataEmprestimo(new Date());
                emprestimoCtrl.getLivro().setQntEstante(emprestimoCtrl.getLivro().getQntEstante() - qnt);
                emprestimoCtrl.setEmprestimo(e);
                emprestimoCtrl.salvarEmprestimo();
                JOptionPane.showMessageDialog(
                        null,
                        "Empréstimo realizado com sucesso!",
                        "Processar empréstimo",
                        JOptionPane.INFORMATION_MESSAGE);

                
                limparAluno();
                limparLivro();
                limparForm();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(
                        null,
                        "Quantidade inválida",
                        "Erro ao processar empréstimo",
                        JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(
                    null,
                    "Dados do empréstimo estão inválidos.",
                    "Erro ao processar empréstimo",
                    JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_botaoProcessarActionPerformed

    private void carregarAluno() {
        Aluno a = null;
        String matricula = textFieldMatricula.getText();
        if (matricula != null && !matricula.isEmpty()) {
            a = alunoCtrl.getDao().pesquisarPorMatricula(matricula);
        } else {
        }
        if (a != null) {
            limparAluno();
            textFieldMatricula.setText(a.getMatricula());
            labelIdAluno.setText(a.getID().toString());
            labelNome.setText(a.getNome());
            labelCpf.setText(a.getCpf());
            labelEmail.setText(a.getEmail());
            labelEndereco.setText(a.getEndereco() + ", " + a.getBairro() + ", " + a.getBairro());
            labelMatricula2.setText(a.getMatricula());
            labelNascimento.setText(new SimpleDateFormat("dd/MM/yyyy").format(a.getDataNascimento()));
            labelRg.setText(a.getRg());
            labelTelefone.setText(a.getTelefone());
            emprestimoCtrl.setAluno(a);
        } else {
            labelErrMatricula.setText("* aluno inexistente");
        }
    }

